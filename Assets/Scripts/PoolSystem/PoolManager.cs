using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class PoolManager : MonoBehaviour
{
    [SerializeField] Pool[] enemyPools;
    [SerializeField] Pool[] playerProjectilePools;
    [SerializeField] Pool[] enemyProjectilePools;
    [SerializeField] Pool[] vFXPools;
    static Dictionary<GameObject, Pool> dictionary;



    void Awake()
    {
        dictionary = new Dictionary<GameObject, Pool>();
        Initialize(playerProjectilePools);
        Initialize(enemyProjectilePools);
        Initialize(vFXPools);
        Initialize(enemyPools);
    }
#if UNITY_EDITOR
    private void OnDestroy()
    {
        CheckPoolSize(playerProjectilePools);
        CheckPoolSize(enemyProjectilePools);
        CheckPoolSize(vFXPools);
        CheckPoolSize(enemyPools);
    }
#endif
    void CheckPoolSize(Pool[] pools)
    {
        foreach(var pool in pools)
        {
            if (pool.RuntimeSize > pool.Size)
            {
                Debug.LogWarning(
                    string.Format("Pool:{0} has a runtime size{1} bigger than its initial size{2}",
                    pool.Prefab.name,
                    pool.RuntimeSize,
                    pool.Size));
            }
        }
    }
    void Initialize(Pool[] pools)
    {
        foreach(var pool in pools)
        {
        #if UNITY_EDITOR
            if (dictionary.ContainsKey(pool.Prefab))
            { 
                Debug.LogError("发现多个相同预制体" + pool.Prefab.name);
                continue;
            }
        #endif
            dictionary.Add(pool.Prefab, pool);

            Transform poolParent= new GameObject("Pool:" + pool.Prefab.name).transform;

            poolParent.parent = transform;
            pool.Initialize(poolParent);
            
        }
    }

    /// <summary>
    /// <para>Return a specified<paramref name="prefab"/><paramef>gameObject in the pool.</para>
    /// <para>根据传入的<paramref name="prefab"/><paramef>参数，返回对象池中预备好的游戏对象</para>
    /// </summary>
    /// <param name="prefab">
    /// <para>Specified gameObject predfab</para>
    /// <para>指定的游戏对象预制体</para>
    /// </param>
    /// <returns>
    /// <para>Prepared gameObject in the pool</para>
    /// <para>对象池中预备好的游戏对象</para>>
    /// </returns>
    /// 
    public static GameObject Release(GameObject prefab)
    {
#if UNITY_EDITOR
        if (!dictionary.ContainsKey(prefab))
        {
            Debug.LogError("无法找到预制体" + prefab.name);

            return null;
        }
#endif
       return dictionary[prefab].PrepareObject();
    }
    #region Release相关重载
    /// <summary>
    /// <para>Return a specified<paramref name="prefab"/><paramef>gameObject in the pool.</para>
    /// <para>根据传入的<paramref name="prefab"/><paramef>参数，返回对象池中预备好的游戏对象</para>
    /// </summary>
    /// <param name="prefab">   
    /// <para>Specified gameObject predfab</para>
    /// <para>指定的游戏对象预制体</para>
    /// </param>
    /// <param name="position">
    /// <para>Specified release position</para>
    /// <para>指定释放位置</para>
    /// </param>
    /// <returns></returns>
    public static GameObject Release(GameObject prefab,Vector3 position)
    {
        #if UNITY_EDITOR
        if (!dictionary.ContainsKey(prefab))
        {
            Debug.LogError("无法找到预制体" + prefab.name);

            return null;
        }
        #endif
        return dictionary[prefab].PrepareObject(position);
    }
    public static GameObject Release(GameObject prefab, Vector3 position,Quaternion rotation)
    {
#if UNITY_EDITOR
        if (!dictionary.ContainsKey(prefab))
        {
            Debug.LogError("无法找到预制体" + prefab.name);

            return null;
        }
#endif
        return dictionary[prefab].PrepareObject(position,rotation);
    }
    public static GameObject Release(GameObject prefab, Vector3 position, Quaternion rotation,Vector3 localscale)
    {
#if UNITY_EDITOR
        if (!dictionary.ContainsKey(prefab))
        {
            Debug.LogError("无法找到预制体" + prefab.name);

            return null;
        }
#endif
        return dictionary[prefab].PrepareObject(position, rotation,localscale);
        
    }
    #endregion
}
